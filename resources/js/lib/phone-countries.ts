export function validatePhone(code: string, digits: string): string | null {
    if (!digits) return 'Mobile Number is required.';
    const rule = PHONE_RULES[code] ?? [6, 12];
    if (digits.length < rule[0] || digits.length > rule[1]) {
        const range = rule[0] === rule[1] ? `${rule[0]}` : `${rule[0]}–${rule[1]}`;
        return `Please enter a valid ${range}-digit number for the selected country.`;
    }
    return null;
}
